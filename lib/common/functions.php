function h($str){
    return htmlspecialchars($str, ENT_QUOTES, 'UTF-8');
}

function sanitize($posts){
    foreach($posts as $key=>$value){
        $after[$key] = htmlspecialchars($value, ENT_QUOTES, 'UTF-8');
    }
    return $after;
}

